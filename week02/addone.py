number = int(input('Please enter a number: '))
newNumber = number + 1

# Created the variables number and newNumber.
# The user will input an integer.
# The int method is used to allow integers, and not characters.

print ('{} + 1 = {}'.format(number, newNumber))
# Both {} will contain the variables listed above, in the order number, newNumber.
# The string that the user's input returns must be converted into an integer, not text.