# This program prints out all even numbers from 2 to 100.
# Author: Jonathon Grealish

numberTo = 100 # the maximum number in the range.
evenNum = 2 # the starting number in the range.

while evenNum <= numberTo:
    print (evenNum)
    evenNum += 2

# The while statement states while evenNum <= numberTo;
# It will output the evenNum (starting at 2).
# This is followed by evenNum being increased in increments of 2, using +=
# The while loop will repeat until we reach the value of numberTo.