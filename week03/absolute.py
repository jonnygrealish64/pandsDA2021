# This program gives the absolute value of a number.
# Author: Jonathon Grealish

number = float(input("Enter a number: "))
absoluteValue = abs(number)

# Float can be used, as there can be decimal numbers involved.
# absoluteValue will output the number's absolute value.

print("The absolute value of {} is {}".format(number, absoluteValue))
