# The variable inputString prompts the user to enter a string.
inputString = input("Enter a string: ")

# The variable lengthOfString uses len to calculate the length of the inputString variable.
# The length is the total number of letters and including spaces.
lengthOfString = len(inputString)

print("The length of {} is {}".format(inputString, lengthOfString))
