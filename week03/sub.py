# This program subtracts one number from another.
# In order to do mathematical calculations, both variables must be converted from str to int.
# Author: Jonathon Grealish

# The int class will read the input as numbers.
x = int(input("Enter first number: "))
y = int(input("Enter second number: "))

# The variable answer will calculate x - y
answer = x-y

print("{} minus {} is {}".format(x,y,answer))
